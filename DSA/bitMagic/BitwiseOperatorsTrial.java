package bitMagic;

public class BitwiseOperatorsTrial {
	public static void main(String [] args)
	{
		int n1=10;
		int n2=12;
		int xor=n1^n2;
		int or=n1|n2;
		int and=n1&n2;
		System.out.println("10 xor 12 is:"+xor);
		System.out.println("10 or 12 is:"+or);
		System.out.println("10 & 12 is:"+and);
	}
}
